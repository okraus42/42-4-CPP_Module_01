#include "Zombie.hpp"

Zombie::Zombie(std::string name)
{
	this->name = name;
}

Zombie::Zombie()
{
	this->name = "noname";
}

Zombie::~Zombie(void)
{
	std::cout << this->name << " was obliterated." << std::endl;
}

void	Zombie::announce(void)
{
	std::cout << this->name << ": BraiiiiiiinnnzzzZ..." << std::endl;
}

void Zombie::nameZ(std::string name)
{
	this->name = name;
}
